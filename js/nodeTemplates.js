/**
 * Get reading for a specific profile and version
 */
function getNodeReading(nodeKey, version) {
  const node = nodeReadings[nodeKey];
  if (!node) return null;
  
  const v = 'v' + version;
  const reading = node[v]?.pages?.length > 0 ? node[v] : node.v1;
  
  // If this profile has no content, fall back to gemini
  if (!reading.pages || reading.pages.length === 0) {
    return {
      title: nodeReadings.gemini.title,
      pages: nodeReadings.gemini.v1.pages
    };
  }
  
  return {
    title: node.title,
    pages: reading.pages
  };
}
